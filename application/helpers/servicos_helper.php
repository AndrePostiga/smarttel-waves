<?php 

public function servicos_id($nome){
	switch ($nome) {
		case 'consultoria':
			return '1'
			break;

		case 'telefonia':
			return '2'
			break;

		case 'cftv':
			return '3'
			break;

		case 'fibra':
			return '4'
			break;

		case 'redes':
			return '5'
			break;

		case 'capacitacao':
			return '6'
			break;		
		
	}

}